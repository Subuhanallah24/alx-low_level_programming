#include "main.h"

/**
 * mul - Multiply two integers
 *
 * @a: The first integer
 * @b: The second integer
 *
 * Description:
 * This function multiplies two integers, @a and @b.
 *
 * Return:
 * Returns the result of the multiplication.
 */

int mul(int a, int b)
{
	int multi;

	multi = a * b;
	return (multi);


}
